package com.ebridge.tevoi.model;

public class PartnerObject
{
    private int id;
    private String Name;
    private String Logo;


    public int getId() {
        return id;
    }

    public String getName() {
        return Name;
    }

    public String getLogo() {
        return Logo;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        Name = name;
    }

    public void setLogo(String logo) {
        Logo = logo;
    }
}
